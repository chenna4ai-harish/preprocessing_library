def _extract_zip_all(zip_path: str, out_dir: str) -> dict:
    """Extract every file from zip_path into out_dir. Returns {lower_name: dest_path}."""
    extracted = {}
    with _zipfile.ZipFile(zip_path, "r") as z:
        for name in z.namelist():
            if name.endswith("/"):
                continue
            dest = os.path.join(out_dir, os.path.basename(name))
            with z.open(name) as src, open(dest, "wb") as dst:
                _shutil.copyfileobj(src, dst)
            extracted[os.path.basename(name).lower()] = dest
    return extracted


def _resolve_and_extract(input_paths: list, out_dir: str):
    """
    For every path in input_paths:
      - If it is a ZIP → extract all its contents to out_dir; remember which
        inner file to use for the union (INNER_FILE or first supported).
      - If it is a plain file → copy to out_dir (avoid self-overwrite).
    Also scan the base directory of every input path for extra ZIPs and extract them.

    Returns:
        all_extracted  : list of all extracted/copied file paths
        union_sources  : list of (file_path, source_label) tuples to be unioned
    """
    os.makedirs(out_dir, exist_ok=True)
    all_extracted: list[str] = []
    union_sources: list[tuple] = []
    scanned_dirs: set = set()
    scanned_zips: set = set()

    for raw_path in input_paths:
        p = raw_path.strip()
        if not p:
            continue

        base_dir = os.path.dirname(os.path.abspath(p))

        # ── Scan base directory for additional ZIPs ────────────────────────
        if base_dir not in scanned_dirs:
            scanned_dirs.add(base_dir)
            for entry in os.scandir(base_dir):
                if entry.name.lower().endswith(".zip") and entry.path not in scanned_zips:
                    if os.path.abspath(entry.path) != os.path.abspath(p):
                        scanned_zips.add(entry.path)
                        extras = _extract_zip_all(entry.path, out_dir)
                        all_extracted.extend(extras.values())

        if _Path(p).suffix.lower() == ".zip":
            scanned_zips.add(os.path.abspath(p))
            extracted = _extract_zip_all(p, out_dir)
            all_extracted.extend(extracted.values())

            # Pick the inner file to union
            if INNER_FILE:
                inner_path = extracted.get(INNER_FILE.lower())
                if inner_path is None:
                    # try case-insensitive match already done; try original name
                    raise ValueError(
                        f"'{INNER_FILE}' not found inside ZIP: {p}. "
                        f"Available: {list(extracted.keys())}"
                    )
            else:
                # first supported file by extension
                inner_path = next(
                    (v for k, v in extracted.items() if _Path(k).suffix.lower() in _SUPPORTED),
                    None,
                )
                if inner_path is None:
                    raise ValueError(f"No supported data file found inside ZIP: {p}")

            union_sources.append((inner_path, os.path.basename(p)))
        else:
            # Plain file — copy to out_dir
            dest = os.path.join(out_dir, os.path.basename(p))
            if os.path.abspath(p) != os.path.abspath(dest):
                _shutil.copy2(p, dest)
            all_extracted.append(dest)
            if _Path(p).suffix.lower() in _SUPPORTED:
                union_sources.append((dest, os.path.basename(p)))

    return all_extracted, union_sources


