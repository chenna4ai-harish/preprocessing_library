def preprocess(input_paths: list) -> list:
    """
    Extract INNER_FILE from each ZIP in input_paths, union them vertically,
    write the merged result; also extract all other ZIP contents to out_dir.

    Parameters
    ----------
    input_paths : list[str]
        Paths to ZIP files (or plain data files) to union.

    Returns
    -------
    list[str]
        All extracted file paths  +  the merged union output path (last element).
    """
    if isinstance(input_paths, str):
        input_paths = [p.strip() for p in input_paths.split(",") if p.strip()]
    if not input_paths:
        raise ValueError("file_union_zip requires at least one input file.")

    out_dir = OUTPUT_DIR if OUTPUT_DIR else os.path.dirname(os.path.abspath(input_paths[0]))
    os.makedirs(out_dir, exist_ok=True)

    all_extracted, union_sources = _resolve_and_extract(input_paths, out_dir)

    if not union_sources:
        raise ValueError("No data files found to union.")

    frames = []
    for file_path, source_label in union_sources:
        df = _load_file(file_path)
        if ADD_SOURCE_TAG and SOURCE_TAG_COLUMN:
            df[SOURCE_TAG_COLUMN] = source_label
        frames.append(df)

    merged = pd.concat(frames, ignore_index=True, sort=False)

    out_path = os.path.join(out_dir, OUTPUT_FILENAME)
    _write_output(merged, out_path, OUTPUT_FORMAT)

    return all_extracted + [out_path]


